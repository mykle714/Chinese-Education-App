/**
 * Word Search — fixed 7×7 placement templates (fallback path).
 *
 * See docs/WORD_SEARCH_TEMPLATES.md for the full design. Used by
 * `generateWordSearchGrid` (wordSearchGrid.ts) once random snaking placement
 * has failed `RANDOM_GRID_ATTEMPTS` whole-grid regenerations in a row.
 *
 * Each template tiles the same fixed 9-hole 7×7 pattern (corners,
 * edge-midpoints, and center — the cells NOT covered by any slot below) into
 * 10 disjoint "slots," each an ordered path of exactly 4 orthogonally-adjacent
 * cells. A word of length 4 fills a whole slot; a shorter word takes a random
 * contiguous run within its slot (see wordSearchGrid.ts), leaving the rest of
 * that slot as ordinary filler.
 *
 * Generated by a one-off backtracking tiling script (not at runtime) and
 * verified for: exactly 10 pieces of exactly 4 cells, every consecutive pair
 * within a piece orthogonally adjacent, no piece cell adjacent to more than 2
 * same-piece cells (rules out T/plus-shaped pieces, which can't be walked as
 * a single path), no overlaps, and full coverage of the 40 non-hole cells.
 */

export interface WordSearchTemplate {
  /** 10 slots, each an ordered path of exactly 4 [row, col] cells. */
  slots: [number, number][][];
}

